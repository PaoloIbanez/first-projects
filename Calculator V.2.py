def add(n1, n2):
  return n1 + n2


def subtract(n1, n2):
  return n1 - n2


def multiply(n1, n2):
  return n1 * n2


def divide(n1, n2):
  return n1 / n2


moreoperations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide,
}
def calculator():
  shouldcontinue = True
  n1 = float(input(f"What is your first number? "))
  for symbol in moreoperations:
    print(symbol)
  while shouldcontinue:
    operations = input(f"With the symbols above select what operation would you like to perform? ")

    n2_ = float(input(f"What is your second number? "))



    calculation_function = moreoperations[operations]
    answer = calculation_function(n1, n2_)
    print(f"{n1} {operations} {n2_} = {answer}")
    if input(f"Type 'y' to continue calculating with {answer}, or type 'n' to start a new calculation: "):
      n1 = answer
    else:
      shouldcontinue = False
      calculator()

calculator()
